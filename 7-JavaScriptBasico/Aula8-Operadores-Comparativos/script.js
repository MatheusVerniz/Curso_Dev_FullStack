/*
    == igualdade valores

    === iguadade valores e tipos

    != diferença valores

    !== diferença e tipos

    > maior que

    < menor que

    >= maior ou igual

    <= menor ou igual

*/

//igualdade valores
let a = 10
let b = 5

console.log(a == b)

//iguadade valores e tipos
let c = 10
let d = '10'

console.log(c === d)

//diferença
let e = 10
let f = '10'

console.log(e !== f)

//maior que
let g = 11
let h = 10

console.log(g > h)

//menor que
let i = 11
let j = 10

console.log(i < j)

//maior ou igual
let k = 10
let l = 5

console.log(k >= l)

//menor ou igual
let m = 10
let n = 5

console.log(n <= m)